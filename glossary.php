								<p><font size="2" face="Arial,Helvetica,Geneva,Swiss,SunSans-Regular"><b>arrangement<br>
										</b></font><font size="2" face="Arial,Helvetica,Geneva,Swiss,SunSans-Regular">A special type of classification for a series of taxa whose phylogenetic history is unknown because no one has ever reconstructed the evolutionary history of the group. Many current classifications are of this nature. Any given arrangement may be either a natural or an artificial classification. Arrangements are necessary systems for classifying taxa until their phylogenetic relationships can be resolved.</font></p>
								<p><font size="2" face="Arial,Helvetica,Geneva,Swiss,SunSans-Regular"><b>category<br>
										</b></font><font size="2" face="Arial,Helvetica,Geneva,Swiss,SunSans-Regular">A relative position in a hierarchical biological classification. The category of a taxon indicates its relative place in the hierarchy of the classification. The Linnaean hierarchy is the most common taxonomic hierarchy and its categories include class, order, family, genus, and species.</font></p>
								<font size="2" face="Arial,Helvetica,Geneva,Swiss,SunSans-Regular">The formation of the names of taxa that occupy certain places in the hierarchy are governed by rules contained in various codes of nomenclature. For example, animal taxa ranked at the level of the category family have names that end in -idae, whereas plant taxa ranked at this level have names that end in -aceae.</font>
								<p><font size="2" face="Arial,Helvetica,Geneva,Swiss,SunSans-Regular">It is very important to remember that the rank of a taxon does not affect its status in the phylogenetic system. To the phylogeneticist, all monophyletic taxa are equally important and all paraphyletic and polyphyletic taxa are equally misleading.</font></p>
								<p><font size="2" face="Arial,Helvetica,Geneva,Swiss,SunSans-Regular">Classifications and arrangements are usually presented as hierarchies of names, with relative position in the hierarchy (rank) noted by categories. These classifications can also be portrayed as tree diagrams</font></p>
								<p><b><font size="2" face="Arial,Helvetica,Geneva,Swiss,SunSans-Regular">character<br>
										</font></b><font size="2" face="Arial,Helvetica,Geneva,Swiss,SunSans-Regular">any observable, measurable, feature or attribute of an organism</font></p>
