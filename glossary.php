								<p><font face="Arial,Helvetica,Geneva,Swiss,SunSans-Regular" size="2"><b>anagenesis<br>
										</b></font><font face="Arial,Helvetica,Geneva,Swiss,SunSans-Regular" size="2">Evolutionary changes in attributes of a species that do not involve branching or speciation.</font></p>
								<p><font face="Arial,Helvetica,Geneva,Swiss,SunSans-Regular" size="2"><b>ancestral species<br>
										</b></font><font face="Arial,Helvetica,Geneva,Swiss,SunSans-Regular" size="2">An ancestral taxon is a species that gave rise to at least one new <b>daughter species</b> during speciation, either through <b>cladogenesis</b> or <b>reticulate speciation</b>. In the phylogenetic system, only species can be ancestral taxa. Groups of species are specifically excluded from being ancestral to other groups of species or to single species. The biological rationale for this distinction is clear; there is an array of processes termed speciation that allow for one species to give rise to another (or two species to give rise to a species of hybrid origin), but there are no known processes that allow for a genus or a family to give rise to other taxa that contain two or more species (&quot;genusation&quot; and &quot;familization&quot; are biologically unknown). <u>Thus, each monophyletic group begins as a single species</u>. <u>This species is the ancestor of all subsequent members of the monophyletic group</u>.</font></p>
