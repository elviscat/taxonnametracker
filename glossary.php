								<p><font size="2" face="Arial,Helvetica,Geneva,Swiss,SunSans-Regular"><strong><b>classification<br>
											</b></strong></font><font size="2" face="Arial,Helvetica,Geneva,Swiss,SunSans-Regular">An organization of taxa into a written system whereby information regarding the relationships of the included taxa can be accurately and precisely retrieved.</font>
<font size="2" face="Arial,Helvetica,Geneva,Swiss,SunSans-Regular">There are three basic kinds of classifications in systematics -<strong> Natural, Artificial, and Arrangements.</strong></font></p>
								<ul>
									<li><font size="2" face="Arial,Helvetica,Geneva,Swiss,SunSans-Regular"><b>natural classifications</b> contain only monophyletic groups and/or species. This type of classification is logically consistent with the phylogenetic relationships of the taxa being classified (as they are currently understood). That is, knowledge claims inherent in a natural classification do not conflict with any knowledge claims inherent in the phylogenetic tree.<br>
											<br>
										</font>
									<li><font size="2" face="Arial,Helvetica,Geneva,Swiss,SunSans-Regular"><b>artificial classifications</b> contain one or more artificial groups (unnatural groups = paraphyletic, polyphyletic). These classifications are logically inconsistent with the phylogenetic relationships of the taxa being classified (as currently understood). Thus, knowledge claims inherent such a classification conflict with knowledge claims in the phylogenetic tree.<br>
											<br>
										</font>
									<li><font size="2" face="Arial,Helvetica,Geneva,Swiss,SunSans-Regular"><b>arrangements</b> are special classifications for taxa whose phylogenetic relationships are unknown. Any given arrangement may be either a natural or an artificial classification. Arrangements are necessary systems for classifying taxa until their phylogenetic relationships can be resolved. (see <b>arrangement</b>)</font>
