								<p><font face="Arial,Helvetica,Geneva,Swiss,SunSans-Regular" size="2"><b>artificial taxon<br>
										</b></font><font face="Arial,Helvetica,Geneva,Swiss,SunSans-Regular" size="2">A taxon that does not correspond to a unit involved in the evolutionary process or to a unit of evolutionary history (see also grade). There two kinds of artificial groups, paraphyletic and polyphyletic groups. Paraphyletic groups are artificial because one or more descendants of an ancestor are excluded from the group. Examples include Vermes and Reptilia. Polyphyletic groups are artificial because the common ancestor is placed in another taxon. Examples include Homeothermia. &quot;Ancestor&quot; is used in its logical sense, i.e., the ancestor is unknown but its inclusion or exclusion can be deduced as a logical consequence of the grouping. The important contrast is between monophyletic groups and nonmonophyletic groups. Paraphyletic groups are as artificial as polyphyletic groups. Further, it is not always possible to distinguish clearly the status of a group as either paraphyletic or polyphyletic.</font></p>
