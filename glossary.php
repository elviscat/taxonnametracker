										</b></font><font size="2" face="Arial,Helvetica,Geneva,Swiss,SunSans-Regular">A natural taxon is a group of organisms that exists in nature as a result of evolution (e.g., natural group). There are many ways that one can group organisms, but only a few groupings comprise natural taxa. In the phylogenetic system, there are two basic kinds of natural taxa: <b>species</b> and <b>monophyletic groups</b>. A species is a lineage. It is a taxon that represents the largest unit of taxic evolution and is associated with an array of processes termed speciation. Members of a monophyletic group share a history of common ancestral relationships not shared with any other taxa placed outside the group.</font></p>
